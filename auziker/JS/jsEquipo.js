var luis = document.getElementById("luis");
var txtLuis = document.getElementById("txtLuis");

luis.addEventListener("mouseenter", function (event) {
    txtLuis.style.visibility = "visible";
});

luis.addEventListener("mouseleave", function (event) {
    txtLuis.style.visibility = "hidden";
});

